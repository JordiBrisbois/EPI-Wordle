// Normalize word (remove accents)
function normalize(word) {
  return word
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z]/g, '');
}

// Check if word is valid (only letters a-z)
function isValidWord(word) {
  return /^[a-zA-Z]{5}$/.test(word);
}

async function importWords() {
  console.log('Importation du dictionnaire francais...');
  
  const db = new sqlite3.Database(dbPath, sqlite3.OPEN_READWRITE);
  
  try {
    // Clear existing words
    await new Promise((resolve, reject) => {
      db.run('DELETE FROM words', (err) => {
        if (err) reject(err);
        else resolve();
      });
    });
    
    // Prepare insert statement
    const insert = db.prepare('INSERT INTO words (word, normalized, is_active) VALUES (?, ?, 1)');
    
    let count = 0;
    const uniqueWords = new Set();
    
    // Process words
    for (const word of FRENCH_WORDS) {
      const normalized = normalize(word);
      
      if (isValidWord(normalized) && !uniqueWords.has(normalized)) {
        uniqueWords.add(normalized);
        await new Promise((resolve, reject) => {
          insert.run(word.toLowerCase(), normalized, (err) => {
            if (err) reject(err);
            else resolve();
          });
        });
        count++;
      }
    }
    
    insert.finalize();
    
    console.log(count + ' mots importes avec succes !');
    console.log('Total de mots dans la base: ' + count);
    
    if (count < 3000) {
      console.log('ATTENTION: Seulement ' + count + ' mots importes (minimum recommande: 3000)');
    }
    
  } catch (error) {
    console.error('Erreur lors de l\'importation:', error);
    process.exit(1);
  } finally {
    db.close();
  }
}

importWords();
